import java.io.DataOutputStream;

public interface CFCSDatatype
{

    public static final int UNDEFINED = 0;

    public static final int ASCII = 1;
    public static final int FLOAT = 2;
    public static final int DOUBLE = 3;
    public static final int BINARY = 4;

    public static final int BYTE = 1;
    public static final int SHORT = 2;
    public static final int INTEGER = 4;
    public static final int OVERSIZE = 8;

    // --------------------------------------------------------------------

    public void setSizingObject(CFCSDataSizing sizing);

    // --------------------------------------------------------------------

    public int getCount();

    // --------------------------------------------------------------------

    public void readData(int index, byte[][] table);

    public void writeData(int index, int count, DataOutputStream stream);

    // --------------------------------------------------------------------

    public void getData(int index, byte[] array) throws CFCSDataSizeError;

    public void setData(int index, byte[] array) throws CFCSDataScaleError;

    public void addData(byte[] array) throws CFCSDataScaleError;

    // --------------------------------------------------------------------

    public void getData(int index, short[] array) throws CFCSDataSizeError;

    public void setData(int index, short[] array) throws CFCSDataScaleError;

    public void addData(short[] array) throws CFCSDataScaleError;

    // --------------------------------------------------------------------

    public void getData(int index, int[] array) throws CFCSDataSizeError;

    public void setData(int index, int[] array) throws CFCSDataScaleError;

    public void addData(int[] array) throws CFCSDataScaleError;

    // --------------------------------------------------------------------

    public void getData(int index, float[] array) throws CFCSDataChannelError;

    public void setData(int index, float[] array) throws CFCSDataChannelError;

    public void addData(float[] array) throws CFCSDataChannelError;

    // --------------------------------------------------------------------

    public void getData(int index, double[] array) throws CFCSDataChannelError;

    public void setData(int index, double[] array) throws CFCSDataChannelError;

    public void addData(double[] array) throws CFCSDataChannelError;

    // --------------------------------------------------------------------

}
