import java.io.DataOutputStream;
import java.io.IOException;

public final class CFCSDatatypeASCII extends CFCSDatatypeBinary
{

    private static final byte END_OF_LINE_CHAR = '\n';

    // --------------------------------------------------------------------

    public final void readData(final int index, final byte[][] cinch)
    {
        final int[] row = new int[cinch.length];

        for (int cell = 0; cell < cinch.length; cell++)
        {
            try
            {
                row[cell] = (new Integer((new String(cinch[cell])).trim())).intValue();
            }
            catch (NumberFormatException exception)
            {
                throw new CFCSError(CFCSSystemError, exception);
            }
        }

        if (index == data.size())
            data.add(row);
        else
            data.set(index, row);
    }

    // --------------------------------------------------------------------

    public final void writeData(final int index, final int count, final DataOutputStream stream)
    {
        final int[] row = (int[]) data.get(index);
        final int[] sizes = sizing.getSizes();

        try
        {
            for (int i = 0; i < count; i++)
            {
                int width = sizes[i];
                byte[] bytes = (Long.toString(row[i] & 0xffffffffL)).getBytes();

                if (width > 0)
                {
                    int pad = Math.max(width - bytes.length, 0);
                    for (int j = 0; j < pad; j++)
                        stream.write((byte) CFCSSystem.PAD_CHAR);
                    for (int j = 0; j < (width - pad); j++)
                        stream.write(bytes[j]);
                }
                else
                {
                    if (i > 0)
                        stream.write((byte) CFCSSystem.VALUE_SEPARATOR_CHAR);
                    stream.write(bytes);
                    if (i == (count - 1))
                        stream.write(END_OF_LINE_CHAR);
                }
            }
        }
        catch (IOException exception)
        {
            throw new CFCSError(CFCSIOError, exception);
        }
    }

    // --------------------------------------------------------------------

}
